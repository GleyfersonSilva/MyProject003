    private void btn_calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_calcularActionPerformed
         
       //Obtendo as notas
       float p1 = Float.parseFloat(txt_p1.getText());
       float p2 = Float.parseFloat(txt_p2.getText());
       
       //Calculando a mÃ©dia considerando peso 4 para p1 e peso 6 para p2
       double media = ((p1 * 0.4) + (p2 * 0.6));
       
       //Formatando a saÃ­da da mÃ©dia
       DecimalFormat m = new DecimalFormat("#.#");
       
       //Mostrando o Resultado
       if (media < 7 ){
       lbl_media.setText(m.format(media));
       lbl_resultado.setText("VocÃª estÃ¡ Reprovado!");
       } else if (media >= 7) {
       lbl_media.setText(m.format(media));
       lbl_resultado.setText("VocÃª estÃ¡ Aprovado!");
       }
    }//GEN-LAST:event_btn_calcularActionPerformed

